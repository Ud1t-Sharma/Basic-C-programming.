#include<stdio.h>
int main()
{// and operator " &&" :- returns true when all the conditions are true.
    printf("%d \n",4<5 && 4<5);
// OR operator " || "  :- returns true even if one of the condition is false.
    printf("%d \n", 5<3 || 5<7);
// NOT operator " ! " :- returns true when the condition is mathematically correct.
    printf("%d \n", !(5>4));
    return 0;
}